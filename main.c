/**	clockwise = 1 -> động cơ quay thuận chieu kim đồng hồ
 * 	clockwise = 0 -> động cơ quay ngược chieu kim đồng hồ
 * 	motor = 1 -> động cơ gắn với các chân OUT1, OUT2, ENA, IN1, IN2
 * 	motor = 2 -> động cơ gắn với các chân OUT3, OUT4, ENB, IN3, IN4
 */
void rotateClockwise(int motor, int clockwise)
{
	if(motor == 1)
	{
    HAL_GPIO_WritePin(GPIOC, GPIO_PIN_10, clockwise);
    HAL_GPIO_WritePin(GPIOC, GPIO_PIN_11, !clockwise);
	}
  if(motor == 2)
  {
    HAL_GPIO_WritePin(GPIOB, GPIO_PIN_14, clockwise);
    HAL_GPIO_WritePin(GPIOB, GPIO_PIN_15, !clockwise);
  }
}

/**
 *  motor 1 ~ PWMA và motor 2 ~ PWMB trên CubeMX
 *  Guide: https://controllerstech.com/pwm-in-stm32/
 * 
 *  Timer clock = APB clock / Prescaler
 *  Freqency = Timer clock / ARR
 *  Duty cycle = (CCR / ARR) x 100 = ? %
 * 
 *  APB clock: xem ở tab Clock Configuration ở file .ioc
 *  ARR: AutoReload Register
 *  CCR: Capture Compare Register
 * 
 *  Tuy trong setting để Prescaler = 71, ARR = 99, nhưng giá trị thực trong tính toán là 72 và 100
 */
void genPWM(int motor, int dutyCycle)
{
  if(motor == 1)
  {
    TIM3->CCR1 = dutyCycle;
  }
  if(motor == 2)
  {
    TIM4->CCR1 = dutyCycle;
  }
}
